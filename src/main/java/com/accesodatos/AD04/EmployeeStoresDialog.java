    public EmployeeStoresDialog(java.awt.Frame parent, boolean modal, Employee selectedEmployee) {
        super(parent, modal);
        initComponents();
        this.setLocationRelativeTo(parent);
        listModel = new DefaultListModel();
        listTitulares.setModel(listModel);

        addElements(DB.getStoresEmployee(selectedEmployee));

        
        if (listModel.isEmpty()) {
            lbError.setText("No est√° asignado a ninguna tienda.");
        }
    }

    private void addElements(ArrayList<String> stores) {
        nStores = stores.size();
        for (String store : stores) {
            listModel.addElement(store);
        }
    }

