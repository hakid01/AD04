    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String stockString = txtFStock.getText();
        

        if (stockString.isEmpty()){
            lbError.setText("Rellena el campo stock");
        } else {
            if (!ValidarCampos.validarEnteroPositivo(stockString)) {
                lbError.setText("Stock incorrecto. Solo n√∫meros enteros positivos.");
            } else {
                int stock = Integer.valueOf(stockString);

                con = DB.connectDatabase(db);

                DB.updateStockItem_Store(con, stock, idSelectedStore, idSelectedItem);

                DB.desconnetDatabase(con);
                
                dispose();
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

