    public void reloadList() {
        removeElements();
        addElements(DB.getStores());
    }

    private void addElements(ArrayList<String> stores) {
        nStores = stores.size();
        for (String store : stores) {
            System.out.println(store);
            listModel.addElement(store);
        }
    }

    private void removeElements() {
        int listLength = nStores;
        if (listLength != 0) {
            for (int i = listLength - 1; i >= 0; i--) {
                listModel.remove(i);
            }
        }
    }

    private void removeSelectedElement() {
        String selectedStore = listStores.getSelectedValue();
        if (selectedStore == null) {
            lbError.setText("Selecciona la tienda a eliminar.");
        } else {
            String[] dataSplit = selectedStore.split(" - ");
            System.out.println("Vamos a borrar la tienda " + selectedStore);
            String[] nameSplit = dataSplit[1].split(", ");
            int respuesta = JOptionPane.showConfirmDialog(null, "Quiere eliminar la tienda \""
                    + nameSplit[0] + "\" de la lista de tiendas?");
            if (respuesta == 0) {

                int idSelectedStore = Integer.valueOf(dataSplit[0]);

                DB.transactionDeleteOfDB(DB.getStore(idSelectedStore));
            }
        }
    }

    private void manageSelectedElement() {
        String selectedStoreString = listStores.getSelectedValue();
        if (selectedStoreString == null) {
            lbError.setText("Selecciona la tienda a gestionar.");
        } else {
            String[] dataSplit = selectedStoreString.split(" - ");
//            System.out.println("Vamos a gestionar la tienda " + selectedStoreString);
//            String[] nameSplit = dataSplit[1].split(", ");
//
//            String nameSelectedStore = dataSplit[1];

            int idSelectedStore = Integer.valueOf(dataSplit[0]);
            
            Store selectedStore = DB.getStore(idSelectedStore);

            StoreItemsOrEmployeesDialog smd = new StoreItemsOrEmployeesDialog(null, true, selectedStore);
            smd.setVisible(true);

        }
    }

