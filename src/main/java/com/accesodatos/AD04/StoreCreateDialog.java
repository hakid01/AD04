    private void addElements(ArrayList<String> provinces) {
        nProvinces = provinces.size();
        for (String province : provinces) {
            comboProvinces.addItem(province);
        }
    }
    private void btnAceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptActionPerformed
        // TODO add your handling code here:
        String name = txtFName.getText();
        String town = txtFTown.getText();
        int indexProvince = comboProvinces.getSelectedIndex();
        String province = comboProvinces.getItemAt(indexProvince);

        String[] provinceSplit = province.split(" - ");
        
        int idSelectedProvince = Integer.valueOf(provinceSplit[0]);
        String nameSelectedProvince = provinceSplit[1];
        
        Province selectedProvince = new Province(idSelectedProvince,nameSelectedProvince);

        if (name.isEmpty() || town.isEmpty()) {
            lbError.setText("Rellena todos los campos");
        } else {
            if (!ValidarCampos.validarNombre(town)) {
                lbError.setText("Nombre ciudad incorrecto. Iniciar con may√∫scula");
            } else {
                
                DB.transactionAddToDB(new Store(name, town, selectedProvince));
                
                dispose();
            }
        }
    }//GEN-LAST:event_btnAceptActionPerformed

